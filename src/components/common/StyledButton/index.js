const StyledButton = (props) => {
  const { label, type, icon, onClick, variant, classes } = props;
  const ButtonIcon =
    typeof icon == 'string' && icons[icon]
      ? icons[icon]
      : icon
      ? () => <i className={`fa ${icon}`}></i>
      : null;
  return (
    <button
      type={type || 'button'}
      className={`${classes || ''} ${styles.rounded_button} ${
        variant === 'dark'
          ? styles.dark_btn
          : variant === 'outline'
          ? styles.outline_dark_btn
          : ''
      }`}
      onClick={onClick && onClick}
    >
      {ButtonIcon ? <ButtonIcon /> : null}
      {label || ''}
    </button>
  );
};

export default StyledButton;
